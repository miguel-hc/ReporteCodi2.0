            txtprovedor6.setText(formatoMoneda(totalprovedor6)+"");
            
            totalcompras = totalprovedor1 + totalprovedor2 + totalprovedor3 + totalprovedor4 + totalprovedor5 + totalprovedor6;
            subtotalcompras.setText(formatoMoneda(totalcompras)+"");

