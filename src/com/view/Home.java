            subtotalL.setText(formatoMoneda(getTotalL())+"");
            
            double total1 = getTotalL() + getTotalGastos() + totalcompras;
            
            subtotaltotal.setText(formatoMoneda(total1)+"");
            
            z.setText(formatoMoneda((double) provedor.getZ(getfecha(),op))+"");
            
